#pragma mark - Conversions
- (NSString *)hexString {
    // Grab the components
    CGFloat red, green, blue, alpha;
    [self getRed:&red green:&green blue:&blue alpha:&alpha];
    
    NSInteger integerRed = red * 255;
    NSInteger integerGreen = green * 255;
    NSInteger integerBlue = blue * 255;
    NSInteger integerAlpha = alpha * 255;
    
    NSString *value = [NSString stringWithFormat:@"#%02x%02x%02x", integerRed, integerGreen, integerBlue];
    if (integerAlpha != 255)
        value = [value stringByAppendingFormat:@"%02x", integerAlpha];
    
    return [value stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceCharacterSet]];
}


#pragma mark - Palette
- (UIColor *)complementaryColor {
    CGFloat hue = 0.0; CGFloat saturation = 0.f; CGFloat brightness = 1.f; CGFloat alpha = 1.f;
    UIColor *complementary;
    
    if ([self getHue:&hue saturation:&saturation brightness:&brightness alpha:&alpha]) {
        CGFloat oppositeHue = fmodf(hue + 0.5f, 1.f);
        complementary = [UIColor colorWithHue:oppositeHue saturation:saturation brightness:brightness alpha:alpha];
    } else if ([self getWhite:&brightness alpha:&alpha]) {
        brightness = 1.f - brightness;
        complementary =  [UIColor colorWithHue:hue saturation:saturation brightness:brightness alpha:alpha];
    }else{
    
    // Error, color wa sin some unknown color space
    complementary = nil;
    }
    return complementary;
}

-(UIColor *)darkerColor{
    
    CGFloat hue = 0.0; CGFloat saturation = 0.f; CGFloat brightness = 1.f; CGFloat alpha = 1.f;
    UIColor *darker;
    
    if ([self getHue:&hue saturation:&saturation brightness:&brightness alpha:&alpha]) {
        brightness = brightness *0.8f;
        darker = [UIColor colorWithHue:hue saturation:saturation brightness:brightness alpha:alpha];
    } else if ([self getWhite:&brightness alpha:&alpha]) {
        brightness = brightness *0.8f;
        darker = [UIColor colorWithWhite:brightness alpha:alpha];
    }else{
        
        // Error, color wa sin some unknown color space
        darker = nil;
    }
    
    return darker;

}

- (UIColor *)contrastingTextColor {
    CGFloat luminance = [self luminance];
    return luminance > 0.5f ? [UIColor blackColor] : [UIColor whiteColor];
}



#pragma mark - Utils
- (CGFloat)luminance {
    CGFloat r, g, b;
    
    // If RGB, grab values, if not then use the white value (gray-scale)
    if (![self getRed:&r green:&g blue:&b alpha:NULL]) {
        [self getWhite:&r alpha:NULL];
        g = r;
        b = r;
    }
    
    // Adjust the components
    if (r <= 0.03928)
        r = r / 12.92f;
    else
        r = powf(((r + 0.055f) / 1.055f), 2.4f);
    
    if (g <= 0.03928f)
        g = g / 12.92f;
    else
        g = powf(((g + 0.055f) / 1.055f), 2.4f);
    
    if (b <= 0.03928f)
        b = b / 12.92f;
    else
        b = powf(((b + 0.055f) / 1.055f), 2.4f);
    
    return 0.2126f * r + 0.7152f * g + 0.0722f * b;
}

